package secao_03_introducao_liguagem_java;

public class Aula29_VariaveisTiposBasicosEmJava {
	
	/**
	 * 
	 * → Variáveis:
	 * 
	 * 		É uma porção de memória (RAM) utilizada para armazenar dados durante a execução dos programas.
	 * 
	 * 
	 * 		→ Uma variável possui:
	 * 
	 * 			• Nome (ou identificador)
	 * 			• Tipo
	 * 			• Valor
	 * 			• Endereço
	 * 
	 * 
	 * 		→ Sintaxe: [tipo] [nome] = [valor inicial]
	 * 
	 * 			• Exemplos: int idade = 25;
	 * 					    double altura = 1.75;
	 * 					    char sexo = 'F';
	 * 
	 * 
	 * 		→ Nomes de variáveis:
	 * 
	 * 			• Não podem começar com dígito: use uma letra ou _
	 * 			• Não pode ter espaço em branco
	 * 			• Não usar acentos ou til
	 * 			• Sugestão: use o padrão "camel case"
	 * 
	 * 			→ Errado:
	 * 
	 * 				• int 5minutos;
	 * 				• int salário;
	 * 				• salário do funcionario;
	 * 			
	 * 			→ Certo:
	 * 
	 * 				• int _5minutos;
	 * 				• int salario;
	 * 				• int salarioDoFuncionario;
	 * 
	 */
	
	
	/**
	 * 
	 * → Tipos primitivos em Java:
	 * 
	 * 		→ Tipos numéricos inteiros:
	 * 
	 * 			• Tipo		• Tamanho		• Valores							• Valor padrão
	 * 
	 * 			byte		8  bits			-128 a 127							0
	 * 			short		16 bits			-32.768 a 32.767					0
	 * 			int			32 bits			-2.147.483.648 a 2.147.483.647		0
	 * 			long		64 bits			-9.223.372.036.854.775.808 a		0L
	 * 									 	 9.223.372.036.854.775.807
	 * 
	 * 
	 * 		→ Tipos numéricos com ponto flutuantes:
	 * 
	 * 			• Tipo		• Tamanho		• Valores							• Valor padrão
	 * 
	 * 			float		32 bits			-1,4024E-37 a 3,4028E+38			0.0f
	 * 			double		64 bits			-4,94E-307 a 3,4028E+38				0.0
	 * 
	 * 
	 * 		→ Um caractere Unicode:
	 * 
	 * 			• Tipo		• Tamanho		• Valores							• Valor padrão
	 * 
	 * 			char		16 bits			'\u0000' a '\uFFFF'					'\u0000'
	 * 
	 * 
	 * 		→ Valor verdade:
	 * 
	 * 			• Tipo		• Tamanho		• Valores							• Valor padrão
	 * 
	 * 			boolean		1 bit			[false, true]						false
	 * 
	 * 
	 * → Tipo String - Cadeia de caracteres (palavras ou textos).
	 * 
	 */

}
