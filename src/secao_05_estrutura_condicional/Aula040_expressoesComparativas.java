package secao_05_estrutura_condicional;

public class Aula040_expressoesComparativas {
	
	/*
	 * → Expressões comparativas:
	 * 
	 * 		São aquelas expressões, como o próprio nome diz, que comparam uma coisa com outra.
	 * 		Ao avaliar uma expressão comparativa e seu resultado é um valor booleano, verdadeira ou falso. 
	 * 
	 * 
	 * 		→ Operadores comparativos:
	 * 
	 * 				• Operador				• Significado
	 * 				   
	 * 					>						maior
	 * 					<						menor
	 * 					>=						maior ou igual
	 * 					<=						menor ou igual
	 * 					==						igual
	 * 					!=						diferente
	 * 
	 */
	
	public static void main(String[] args) {
		
//		Variáveis:
		
		int x = 5;
		
		System.out.println(x > 10);
		System.out.println(x < 10);
		System.out.println(x >= 10);
		System.out.println(x <= 10);
		System.out.println(x == 10);
		System.out.println(x != 10);
		
	}

}
