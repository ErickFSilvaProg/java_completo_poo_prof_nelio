package secao05_estrutura_condicional;

public class Aula033_Expressoes_comparativas {
	
	public static void main(String[] args) {
		
		/*
		 * São expressões que comparam um 'uma coisa' com 'outra coisa'.
		 * Essa comparação retorna um valor verdade. True ou False.
		 * 
		 * - Operadores comparativos:
		 * 
		 * 		>  - Maior
		 * 		<  - Menor
		 * 		>= - Maior ou igual
		 * 		<= - Menor ou igual
		 * 		== - Igual
		 * 		!= - Diferente
		 */
		
		int X = 5;

		System.out.println(X > 0);
		System.out.println(X == 3);
		System.out.println(10 <= 30);
		System.out.println(X != 2);
		
	}

}
